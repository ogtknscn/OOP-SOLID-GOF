//Single Responsibility Principle (SRP)
// Definition: A class should have only one reason to change, meaning that a class should have only one job.

//Open - Closed Principle (OCP)
// Definition: Objects or entities should be open for extension but closed for modification.

//Liskov Substitution Principle (LSP)
// Definition: Let q(x) be a property provable about objects x of type T. Then q(y) should be provable for objects y of type S where S is a subtype of T.